# output the integer number
puts 1;         # => 1
puts 1 + 2;     # => 3
# you can also use the parentheses
puts(1+2);      # => 3
3+5;            # it just evaluates, not print the output

# output the string
puts "Hello world";
puts            # EMPTY STRING
puts "Iam the genius hacker";

# output the floating number
puts 3.1459;

# you can use the seperated comma
puts "this line: ", "twenty";